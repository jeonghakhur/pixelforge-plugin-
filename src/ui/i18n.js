export var lang = 'ko';

export function t(path) {
  var parts = path.split('.');
  var obj = i18n[lang];
  for (var i = 0; i < parts.length; i++) {
    if (!obj) return path;
    obj = obj[parts[i]];
  }
  return obj || path;
}

export function applyLang() {
  document.querySelectorAll('[data-i18n]').forEach(function (el) {
    el.textContent = t(el.dataset.i18n);
  });
  document.querySelectorAll('[data-i18n-placeholder]').forEach(function (el) {
    el.placeholder = t(el.dataset.i18nPlaceholder);
  });
  if (state.extractedData) {
    var meta = state.extractedData.meta;
    var metaModeEl = document.getElementById('metaMode');
    if (metaModeEl) {
      metaModeEl.textContent =
        meta.sourceMode === 'selection' ? t('extract.scopeSelection') : t('extract.scopeAll');
    }
  }
}

// lang 변경 시 외부에서 등록한 콜백을 호출 (a11y renderMatrix 등)
var _onLangChange = null;
export function registerLangChangeCallback(cb) {
  _onLangChange = cb;
}

// ── Lang toggle ──
document.querySelectorAll('.lang-btn').forEach(function (btn) {
  btn.addEventListener('click', function () {
    lang = btn.dataset.lang;
    document.querySelectorAll('.lang-btn').forEach(function (b) {
      b.classList.toggle('active', b.dataset.lang === lang);
    });
    applyLang();
    if (_onLangChange) _onLangChange();
  });
});
